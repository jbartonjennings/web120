<p>Galleries are an essential element to many business websites. Galleries can be created via CSS or Javascript. In general, it seems that galleries formatted entirely in CSS are less dynamic and more simple than what is possible with Javascript. Many pre-built JQuery galleries are available. There are a lot of highly functional and attractive galleries available for free, but the finest and most complex options are often premium code that must be paid for. Paid galleries can function more reliably and potentially drive more traffic to a website. </p>
<p>JQuery plugins may be customized to the type of device being use, with separate mobile and desktop plugins; while others are responsive and adapt between devices. Mobile-specific plugins are likely to provide higher functionality and sleeker formatting, but require additional coding between devices. Going forward, it seems that mobile-specific plugins will become superfluous and the responsiveness of a well-built gallery will be optimized for all screen sizes. </p>
<p>Galleries can come in several forms, including sliders/carousels, lightboxes and thumbnail galleries. The type of gallery utilized will depend on the business case of the site. Sliders are useful for displaying important content upon landing on a page; a lightbox is a pop-up on top of the current page and must be navigated before returning to the main content; thumbnail galleries are useful for displaying large quantities of content, making it easy to navigate between images quickly. </p>
<p>In addition to photographs and other images, galleries can be used to contain iframes, HTML5 video, href video (YouTube, Vimeo, etc), text and audio files. </p>
<p>For search engine optimization, every image in a gallery should be given a description or caption. For ease of use and loading time, it is recommended that the total number of photos included in a gallery is limited to 12-15 images. If additional content is required, it should be distributed into several separate galleries throughout the site. </p>

