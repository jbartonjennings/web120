<p>Parallax is a term used to describe the effect of multiple elements moving at different speeds on the display of a website. While the technology for parallax displays has been available for decades, it has only recently caught on as a common website design feature. </p>
<p>Parallax sites have several potential advantages: it may present your business’ site as professional, fun, and cutting-edge; it is possible that parallax effects will entice users to spend more time viewing your page, as they are likely to scroll through the entire display once they begin; it may increase engagement with the site overall. However, there are several distinct disadvantages to parallax design: a small percentage of users may experience motion-sickness when scrolling, compelling them to leave the site; parallax is a relatively complex system to install in a site and it may not actually improve site retention or engagement, but may require more cost and time for development and maintenance of a site; it is not generally effective on mobile devices, so is typically turned off when viewed on small screens; parallax may slow down the loading time of your page, causing visitors to leave the site before even viewing the effect. </p>
<p>Parallax can be written into a site using a combination of html, css and javascript. There are many sites online that contain step-by-step instructions on properly coding a parallax effect for your site. In addition, there are a large number of parallax plugins available. The majority of plugins are created with javascript and must be installed into a site by someone familiar with html, css and js functionality. There are a wide range of Wordpress-specific plugins, and as of early 2018, many Wordpress, Wix and Squarespace themes include parallax effects and can be quickly adopted if using one of these platforms. </p>
<p>Parallax sites can have numerous effects including content fade-in, fade-out, and curtains. While it is possible to create horizontally scrolling parallax, the vast majority of sites implement vertical scrolls, as these appear to be preferred by users.  
</p>

