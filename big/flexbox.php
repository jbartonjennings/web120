<p>The flexbox is an excellent tool for responsive web design. The flexbox element provides much more dynamism than other css elements and fluidly handles all types of content across device sizes. </p>
<p>The flexbox operates on a few relatively simple concepts. The element’s setup is dictated by the use of two axis: the main axis and the cross axis. Either can be horizontal or vertical, and the writing of css rules for the element will hinge on this designation. The flexbox is made up of a parent element known as the flex-container and children elements known as flex-items. </p>
<p>Going forward, it will be important to keep an eye on how the integration of the Box Alignment Specification will interact with the flexbox. This spec manages layout for all elements, including those not in a flexbox element. According to MDN “The reason that the Box alignment properties remain detailed in the flexbox specification as well as being in box alignment is to ensure that completion of the flexbox spec is not held up by box alignment, which has to detail these methods for all layout types. There is a note in the flexbox spec stating that in the future, once it is completed, the definitions in Box Alignment Level 3 will supercede those of flexbox.”</p>
<p>Like all aspects of css, the flexbox will continue to develop and integrate new properties. However, the current model is incredibly useful for mobile applications and responsive web page design and should be considered for any content that will be used on mobile devices. </p>

