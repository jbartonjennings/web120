<p>As of early 2019, it appears that many small online retailers choose from a relatively small set of shopping cart software programs. Based on my research, it appears that Shopify is likely the most popular choice at this point in time. More comprehensive small-business platforms such as Wix and Squarespace do seem to be gaining in popularity. In particular, the e-commerce functions of Wix seems to be highly regarded. </p>
